<p><strong>Main duties and responsibilities </strong></p>

<p>11. Internal Revenue Department sets 8 policies and 5 missions in order to ensure full tax collection, to reform the tax administration system to be in line with Market Economy System and to regulate money circulation and inflation with the tax administration system.</p>

<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (a)&nbsp;&nbsp;&nbsp;&nbsp; Policy</strong></p>

<p style="margin-left:1.5in;">&nbsp;(1) &nbsp;&nbsp;&nbsp;&nbsp; To establish a firm legal framework which enables the Internal Revenue Department to work on the processes effectively</p>

<p style="margin-left:1.5in;">&nbsp;(2) &nbsp;&nbsp;&nbsp;&nbsp; Re-organize the Internal Revenue Department to be in line with the new administration procedures</p>

<p style="margin-left:1.5in;">&nbsp;(3) &nbsp;&nbsp;&nbsp;&nbsp; To modernize tax assessment processes</p>

<p style="margin-left:1.5in;">&nbsp;(4)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To utilize technology fully in order to simplify and organize processes and procedures</p>

<p style="margin-left:1.5in;">&nbsp;(5)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To utilize Service and Enforcement programs in order to manage risks</p>

<p style="margin-left:1.5in;">&nbsp;(6) &nbsp;&nbsp;&nbsp;&nbsp; To develop the staff in order to enable them to use their capability and capacity to the full extent</p>

<p style="margin-left:1.5in;">&nbsp;(7)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To develop leadership and governance programs to ensure the reforms are managed</p>

<p style="margin-left:1.5in;">&nbsp;(8)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To increase transparency and accountability in all aspects of administration</p>

<p>&nbsp;</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (b)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Mission</strong></p>

<p style="margin-left:1.5in;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To facilitate the socio-economic development of the country and the people by strengthening tax assessment in line with the laws in order to ensure full tax collection&nbsp;</p>

<p style="margin-left:1.5in;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To assist the taxpayers with taxpayer education programs and other services to increase voluntary compliance and to change to self-assessment system</p>

<p style="margin-left:1.5in;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (3) &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To build trust by doing tax assessment accurately and truthfully in accordance with the tax laws</p>

<p style="margin-left:1.5in;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (4)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; to upgrade the tax offices to enable the use of modern technologies and to work effectively and systematically</p>

<p style="margin-left:1.5in;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (5)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; To provide staff with training opportunities to consistently develop skills, experience and knowledge</p>

<p>12. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The main objective is to collect revenue to cover the expenditure of the government and to facilitate the economic development, but revenue collection also aims to implement the following objectives -&nbsp;</p>

<ul>
	<li>To steady the budgeting system and to regulate money circulation and inflation</li>
	<li>To stabilize domestic consumption, investment and saving</li>
	<li>To stabilize commodity prices</li>
	<li>To balance distribution of wealth</li>
	<li>To facilitate economic development</li>
	<li>To facilitate the implementation of the economic objectives of the State</li>
	<li>To protect domestic industrial businesses</li>
</ul>

