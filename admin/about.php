<p><strong>Gradual Changes in organization structure </strong></p>

<p>6.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In implementation of the New Administration System and in exercising the powers conferred under the Notification No. 97, dated 15-3-1972, of the Revolutionary Council, Internal Revenue Department was established on 1.10.1972 with 279 officer positions and 3043 staff positions, totalling 3322 positions.</p>

<p>&nbsp;</p>

<p>7. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; With the approval of the Cabinet Meeting No. 12/2004 of the State Peace and Development Council on 18-3-2004, Internal Revenue Department was restructured with 583 officer positions and 4160 staff positions, totalling 4743 positions.</p>

<p>&nbsp;</p>

<p>8. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; With the approval of the Cabinet Meeting No. 2/2012 of the Government of the Union of Myanmar on 12-1-2012, Internal Revenue Department was restructured with 778 officer positions and 4122 staff positions, totalling 4900 positions.</p>

<p>9. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; With the approval of the Cabinet Meeting No. 12/2014 of the Government of the Union of Myanmar on 19-6-2014, Internal Revenue Department was restructured with 1091 officer positions and 5186 staff positions, totalling 6259 positions.</p>

<p>10. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; With the decision of the Cabinet Meeting No. 1/2016 of the Republic of the Union of Myanmar on 7-1-2016, the organization structure of Internal Revenue Department was redesigned without changing the total number of manpower -1091 officer positions and 5186 staff positions, totalling 6259 positions.</p>

