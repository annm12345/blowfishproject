<p><strong>Reform</strong><strong> Activities&nbsp;&nbsp; </strong></p>

<p>14. Internal Revenue Department has carried out policy reforms and procedural reforms.</p>

<p><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (a)&nbsp;&nbsp;&nbsp;&nbsp; Policy Reform</strong></p>

<p style="margin-left:1.5in;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; As a policy reform of Internal Revenue Department,&nbsp; Large Taxpayer Office (LTO) was established on 1-4-2014 in order to change to the self-assessment system (SAS) from the previous practice of the official assessment system (OAS).</p>

<p style="margin-left:1.5in;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The self-assessment system (SAS) is also practiced in Medium Taxpayer Office 1 from 1-4-2017.</p>

<p style="margin-left:1.0in;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (b)&nbsp;&nbsp;&nbsp;&nbsp; Progress and Reforms which have been made </strong></p>

<p style="margin-left:1.5in;">&nbsp;(1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Seeking financial and technical assistance from International Orgainzations such as International Monetary Fund, World Bank and Office of Technical Assistance, US Department of Treasury to implement modern taxation system</p>

<p style="margin-left:1.5in;">&nbsp;(2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Implementation of Tax Reform Programs with financing and grants provided toward Modernization of Public Finance Management Project of the World Bank</p>

<p style="margin-left:1.5in;">&nbsp;(3)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; State Lottery Department increased the amount of lottery award to 1,500 lakhs mmk as a special award from Draw No. 277 (Draw Date: 1-2-2012) onwards. &nbsp;</p>

<p style="margin-left:1.5in;">(4)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Established Board of Scrutinizing and Monitoring of Tax Collection by the Notification No. 31/2013 dated 8-5-2013 to scrutinize, in collecting the tax, whether taxes are collected properly or not and to give suggestions to the Minister of Finance on current taxation system for ensuring proper tax collection and meeting international standards</p>

<p style="margin-left:1.5in;">(5) &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Established Taxpayer Service Units in Yangon Region on (19-10-2012) and in Mandalay Region on (20-5-2013) in order to provide answers to the questions raised by the people with regards to tax matters; to assist in submitting the complaints relating to tax matters; to setttle the complaints and to provide tax information to the foreign investors</p>

<p style="margin-left:1.5in;">(6)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Hold Tax Reform Program Steering Committee meeting once a month from 2015 February onward</p>

<p style="margin-left:1.5in;">(7)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In order to collect the actual tax from restaurants, Tax Label Project was initiated (Affixing tax labels equivalent to the amount of commercial tax on the receipts to the customers) as a pilot project in Yangon Development Council area from 1 November 2014 onwards and continued in the whole country from 1 April 2015 onwards to have tax labels affixed on the receipts issued by restaurants and hotels</p>

<p style="margin-left:1.5in;">(8)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; With the approval of the Cabinet Meeting No. 4/2015 of the Union Government, the Ministry of Finance issued Notification No. 628/2015 dated 9-3-2015 to form <a name="_Hlk486411363" id="_Hlk486411363">revenue-raising steering committee</a>, region and state level <a name="_Hlk486411372" id="_Hlk486411372">revenue-raising workgroups </a>and township/ village level revenue-raising workgroups.</p>

<p style="margin-left:1.5in;">(9)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Transformed Company Circle Tax Office to Medium Taxpayer Offices and established Medium Taxpayer Office 1 and 2 on 11-6-2015</p>

<p style="margin-left:1.5in;">&nbsp;(10)&nbsp; &nbsp; Established Medium Taxpayer Office 3 by the Notification No. 054 of Internal Revenue Department of the Ministry of Finance dated 10-3-2016</p>

<p style="margin-left:1.5in;">&nbsp;(11)&nbsp;&nbsp;&nbsp; According to the Letter of the Accounts Directorate of the Internal Revenue Department dated 30-5-2016 with the letter no.1(8)/accounts-1/patakha/2016(539), licensed stamped paper resellers are appointed. On 1-6-2016, 298 resellers were permitted to resell stamped paper in 93 townships.&nbsp;</p>

<p style="margin-left:1.5in;">(12)&nbsp;&nbsp;&nbsp;&nbsp; State Lottery Directorate of the Internal Revenue Department will start selling 500-kyat lottery tickets in March 2017 and the first draw will be on April 1<sup>st</sup>.</p>

