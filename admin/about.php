<p style="margin-left:1.5in;">(1)&nbsp;&nbsp;&nbsp;&nbsp; A new law enacted by Pyithu Hluttaw in accordance with Socialist Economic System. It was to assess tax on the income of individuals and private sector. Moreover, tax on imports and entertainment and tax due on production of salt were assessed by Profit Tax Law effective from 1-4-76. Income Tax law was not applied to individual business owners from 1-4-76. No books and records were required for tax assessment. There were 4 amendments of Profit Tax Law. Profit Tax Law was amended in 1986, 1987, 1989 and 1991. Profit Tax regulations were added in 1987.</p>

