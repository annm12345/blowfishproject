<p align="center"><strong><span style="font-size:18px;">Internal Revenue Department</span> </strong></p>

<p><span style="font-size:16px;"><strong>Background</strong></span></p>

<p>1. The Administration System in the Union of Myanmar was a slightly modified Colonial administration system. It was not effectively beneficial to the country and the people. Thus, the implementation of new Administration System was required and the Revolutionary Council issued Notification No. 97 dated March 15, 1972 which enacted new procedures for restructuring of administration mechanisms.</p>

<p>&nbsp;</p>

<p>2. According to the Notification No. 97, in order to implement new administration system, Ministry of Planning and Finance merged Profit Tax Department, Commercial Tax Department, Excise Department, State Lottery Department, Stamp Duty Department, the Revenue Section of the General Administration Department, and the Office of the Financial Commissioner which existed separately and formed the Internal Revenue Department on 1-10-1972 in order to administer all tax assessments.</p>

<p align="center">&nbsp;</p>

<p>3. The first offices were 1 Internal Revenue Department (headquarters), 6 Directorates, 14 Region/State offices, 250 township offices â€“ totalling 271 offices. The headquarter was in Yangon. At that time, the title of the head of the department was Chairman. Later on, it was changed to Director General. Internal Revenue Department administered 10 kinds of taxes and duties, namely income tax, profit tax, commercial tax, excise tax, State lottery tax, stamp duty, land tax, water tax and embankment tax, fisheries tax and mineral tax (crops tax in Shan State).</p>

<p>&nbsp;</p>

<p>4. Socialist Economic System was adopted before 1988 and Market Economy System was adopted after 1988 during Military Government Era. In order to ensure more effective tax collection system, five kinds of taxes and duties administered by the IRD were transferred to other relevant departments. Land tax, water tax and embankment tax, excise tax and mineral tax were transferred to the General Administration Department and fisheries tax was transferred to Fisheries Department on 1-4-1989.</p>

